# Karakteristik Algoritma
# - Terbatas / finiteness = proses dalam algoritma harus memiliki akhir dan jumlah langkah terbatas dan menghasilkan minimal sebuah output

# - tidak ambigu ( definiteness ) = ambigu (beberapa), tidak ambigu ( ambilah 5 angka)

# - Input dan Output = algoritma harus menerima nol atau lebih input dan setidaknya menghasilakan 1 Output

# - Efektif ( effectiveness ) = langkah - langkah dalam algortima harus efektif dan sesederhana mungkin