// Operator simbol dalam program yang digunakan untuk merubah suatu nilai

// 1. Operator Aritmatika
// Penjelasan:
// Operator aritmatika digunakan untuk melakukan operasi matematika seperti penjumlahan, pengurangan, perkalian, pembagian, dll. Operator ini bekerja dengan tipe data numerik seperti integer dan float.

// Daftar Operator Aritmatika:

// Operator	Nama	Contoh	Hasil
// +	Penjumlahan	5 + 3	8
// -	Pengurangan	5 - 3	2
// *	Perkalian	5 * 3	15
// /	Pembagian	5 / 2	2.5
// //	Pembagian Bulat	5 // 2	2
// %	Modulus (Sisa)	5 % 2	1
// **	Pangkat (Eksponen)	5 ** 2	25
// Contoh Kode (Python):
// python
// Copy code
// a = 10
// b = 3
// print(a + b)  # Output: 13
// print(a - b)  # Output: 7
// print(a * b)  # Output: 30
// print(a / b)  # Output: 3.3333
// print(a % b)  # Output: 1


// 2. Operator Logika
// Penjelasan:
// Operator logika digunakan untuk menggabungkan atau memanipulasi nilai logika (boolean). Nilai logika terdiri dari True (benar) dan False (salah). Operator ini sering digunakan dalam pernyataan kondisi atau pengulangan.

// Daftar Operator Logika:

// Operator	Nama	Contoh	Hasil
// and	Logika AND	True and False	False
// or	Logika OR	True or False	True
// not	Logika NOT	not True	False
// Contoh Kode (Python):
// python
// Copy code
// x = True
// y = False
// print(x and y)  # Output: False
// print(x or y)   # Output: True
// print(not x)    # Output: False


// 3. Operator Relasional (Perbandingan)
// Penjelasan:
// Operator relasional digunakan untuk membandingkan dua nilai dan menghasilkan nilai boolean (True atau False). Operator ini sering digunakan dalam pengambilan keputusan (if-else) atau perulangan.

// Daftar Operator Relasional:

// Operator	Nama	Contoh	Hasil
// ==	Sama dengan	5 == 5	True
// !=	Tidak sama dengan	5 != 3	True
// <	Lebih kecil	3 < 5	True
// >	Lebih besar	5 > 3	True
// <=	Lebih kecil atau sama dengan	3 <= 3	True
// >=	Lebih besar atau sama dengan	5 >= 3	True
// Contoh Kode (Python):
// python
// Copy code
// a = 10
// b = 5
// print(a == b)  # Output: False
// print(a != b)  # Output: True
// print(a > b)   # Output: True
// print(a <= b)  # Output: False